  ->condition('field_original_fc_id', $original_fc_id);
  $pids = $query->execute();
  $paragraphs = $para_storage->loadMultiple($pids);

  print("\rORIGINAL: $original_fc_id");

  foreach ($paragraphs as $p) {
    print( "\r PARAGRAPH ID: ".$p->id());            // "Lorem Ipsum..."
  }
}



