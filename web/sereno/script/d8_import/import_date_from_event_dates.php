$counter = 0;

foreach ($d8_nodes as $node_id => $dates) {

  print ('Updating node: '. $node_id);
  $date = $dates['field_event_dates'];
  $granularity = $date['granularity'];

  if ($node = entity_load('node', $node_id)) {

    $date['year'] = str_pad($date['year'], 4, '0', STR_PAD_LEFT);
    $date['month'] = str_pad($date['month'], 2, '0', STR_PAD_LEFT);
    $date['day'] = str_pad($date['day'], 2, '0', STR_PAD_LEFT);
    $date['year_to'] = str_pad($date['year_to'], 4, '0', STR_PAD_LEFT);
    $date['month_to'] = str_pad($date['month_to'], 2, '0', STR_PAD_LEFT);
    $date['day_to'] = str_pad($date['day_to'], 2, '0', STR_PAD_LEFT);

    if ($granularity == 'year') {
      $value = $date['year'] . '-' . '01' . '-' . '01';
      $end_value = $date['year_to'] . '-' . '01' . '-' . '01';
    }
    else {
      if ($granularity == 'month') {
        $value = $date['year'] . '-' . $date['month'] . '-' . '01';
        $end_value = $date['year_to'] . '-' . $date['month_to'] . '-' . '01';
      }
      else {
        $value = $date['year'] . '-' . $date['month'] . '-' . $date['day'];
        $end_value = $date['year_to'] . '-' . $date['month_to'] . '-' . $date['day_to'];
      }
    }
    //print $value.' - '.$end_value;
    $node->set('field_ipu_event_dates', [
      [
        'value' => ($value),
        'end_value' => ($end_value),
        //'value_all_day' => TRUE,
        //'end_value_all_day' => TRUE
      ]
    ]);
    $node->set('field_date_accuracy', $granularity);
    //$node->set('field_event_dates_value_all_day', true);
    //$node->set('field_publication_document', []); // remove all paras from this node
    $counter++;

    $node->save();
    print("\n - Updated node $node_id \n");
  } else {
    print("\n - Node $node_id not found\n");
  }
}

